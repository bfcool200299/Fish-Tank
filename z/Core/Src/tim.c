void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* tim_baseHandle)
{
	
	if(tim_baseHandle->Instance == htim2.Instance)
	{
		
		if(DuojiLock == 1)
		{
			Time_Duo++;
			if(Time_Duo%500 == 0 && Duoji_Mode == 1)
			{
				D_Mode_1 = 1;
				Time_Duo = 0;
			}
			if(Time_Duo%800 == 0 && Duoji_Mode == 2)
			{
				D_Mode_2 = 1;
				Time_Duo = 0;
			}
			if(Time_Duo%1200 == 0 && Duoji_Mode == 3)
			{
				D_Mode_3 = 1;
				Time_Duo = 0;
			}
			if(Time_Duo%1800 == 0 && Duoji_Mode == 4)
			{
				D_Mode_4 = 1;
				Time_Duo = 0;
			}
			if(Time_Duo%2300 == 0 && Duoji_Mode == 5)
			{
				D_Mode_5 = 1;
				Time_Duo = 0;
			}
			if(Time_Duo%2800 == 0 && Duoji_Mode == 6)
			{
				D_Mode_6 = 1;
				Time_Duo = 0;
			}
		}
		
		Time++;
		
		if(Time % 1500 == 0)
		{
			HUNZHUOFlag = 1;
			LanYaFlag = 1;
			TDSFlag = 1;
		}
		
		if(Time % 1200 == 0)
		{
			RGB1sFlag = 1;
		}
		
		if(Time % 30 == 0)
		{
			RGB20msFlag = 1;
		}
		
		if(Time % 750 == 0)
		{
			Ds18b20Flag = 1;
			BH1750Flag = 1;
		}
		
	}

}
